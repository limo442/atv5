public class Operadores_atribuicao {

    public float adicao_atribuicao(float v1, float v2) {
        return v1 += v2;
    }

    public float subtracao_atribuicao(float v1, float v2) {
        return v1 -= v2;
    }

    public float multiplicacao_atribuicao(float v1, float v2) {
        return v1 *= v2;
    }

    public float divisao_atribuicao(float v1, float v2) {
        return v1 /= v2;
    }
}
